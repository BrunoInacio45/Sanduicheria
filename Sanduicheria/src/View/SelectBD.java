    private void SubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitButtonActionPerformed
        try {
//            c.setSenhaBanco(BdPassword.getText());
//            c.setUsuarioBanco(BdUser.getText());
            ConexaoBancoDeDados c = null;

            if (MysqlRadio.isSelected() && !PostRadio.isSelected()) {
                c = ConexaoBancoDeDados.getInstance("mysql", BdUser.getText(), BdPassword.getText());
            } else if (!MysqlRadio.isSelected() && PostRadio.isSelected()) {
                c = ConexaoBancoDeDados.getInstance("psql", BdUser.getText(), BdPassword.getText());
            } else {
                JOptionPane.showMessageDialog(null, "Selecione os bancos corretamente");
            }
            if (c.getC() == null) {
                JOptionPane.showMessageDialog(null, "Por favor verifique os dados de conexao");
                c.clearInstance();
            } else {
                JOptionPane.showMessageDialog(null, "Conexao Realizada com sucesso!");
                new TelaLogin().setVisible(true);
                this.dispose();

            }
//            System.out.println(c.getC());
        } catch (Exception e) {
            System.out.println(e.getMessage());

        }

    }//GEN-LAST:event_SubmitButtonActionPerformed

